CREATE OR REPLACE PACKAGE BODY scheduler_monitoring AS
  PROCEDURE write_log(
    p_operation_name IN VARCHAR2,
    p_start_ts       IN TIMESTAMP,
    p_status         IN VARCHAR2,
    p_error_message  IN CLOB DEFAULT NULL
  );

  FUNCTION parse_ts(p_text IN VARCHAR2) RETURN TIMESTAMP WITH TIME ZONE IS
  BEGIN
    IF p_text IS NULL THEN
      RETURN NULL;
    END IF;

    BEGIN
      RETURN TO_TIMESTAMP_TZ(p_text, 'YYYY-MM-DD"T"HH24:MI:SS.FF TZH:TZM');
    EXCEPTION
      WHEN OTHERS THEN
        BEGIN
          RETURN TO_TIMESTAMP_TZ(p_text, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM');
        EXCEPTION
          WHEN OTHERS THEN
            RETURN TO_TIMESTAMP_TZ(
                     p_text || ' ' || TO_CHAR(SYSTIMESTAMP, 'TZH:TZM'),
                     'YYYY-MM-DD HH24:MI:SS TZH:TZM'
                   );
        END;
    END;
  END parse_ts;

  FUNCTION clamp_top_n(p_top_n IN NUMBER, p_default IN NUMBER, p_max IN NUMBER) RETURN NUMBER IS
  BEGIN
    RETURN LEAST(GREATEST(NVL(p_top_n, p_default), 1), p_max);
  END clamp_top_n;

  FUNCTION sanitize_granularity(p_val IN VARCHAR2) RETURN VARCHAR2 IS
    l_val VARCHAR2(20) := UPPER(TRIM(NVL(p_val, 'HOUR')));
  BEGIN
    IF l_val NOT IN ('MINUTE', 'HOUR', 'DAY') THEN
      RETURN 'HOUR';
    END IF;
    RETURN l_val;
  END sanitize_granularity;

  FUNCTION sanitize_status(p_val IN VARCHAR2) RETURN VARCHAR2 IS
    l_val VARCHAR2(40) := UPPER(TRIM(p_val));
  BEGIN
    IF l_val IS NULL THEN
      RETURN NULL;
    END IF;
    IF l_val IN ('SUCCEEDED','FAILED','RUNNING','RETRY SCHEDULED','STOPPED','BROKEN') THEN
      RETURN l_val;
    END IF;
    RETURN NULL;
  END sanitize_status;

  FUNCTION format_duration(p_seconds IN NUMBER) RETURN VARCHAR2 IS
    l_secs NUMBER := NVL(p_seconds, 0);
  BEGIN
    IF l_secs < 60 THEN
      RETURN TO_CHAR(ROUND(l_secs, 3)) || ' sec';
    ELSIF l_secs < 3600 THEN
      RETURN TO_CHAR(ROUND(l_secs / 60, 3)) || ' min';
    ELSIF l_secs < 86400 THEN
      RETURN TO_CHAR(ROUND(l_secs / 3600, 3)) || ' hr';
    ELSE
      RETURN TO_CHAR(ROUND(l_secs / 86400, 3)) || ' day';
    END IF;
  END format_duration;

  FUNCTION get_database_current_time RETURN CLOB IS
    l_json CLOB;
  BEGIN
    SELECT JSON_OBJECT(
      'database_systimestamp' VALUE TO_CHAR(SYSTIMESTAMP, 'YYYY-MM-DD\"T\"HH24:MI:SS.FF TZH:TZM'),
      'database_sysdate' VALUE TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS'),
      'session_timezone' VALUE SESSIONTIMEZONE,
      'dbtimezone' VALUE DBTIMEZONE
    )
    INTO l_json
    FROM dual;

    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END get_database_current_time;

  FUNCTION list_all_jobs(
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_top_n            IN NUMBER DEFAULT 1000
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_top_n NUMBER := clamp_top_n(p_top_n, 1000, 10000);
  BEGIN
    SELECT JSON_OBJECT(
      'summary' VALUE (
        SELECT JSON_OBJECT(
          'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
          'total_jobs' VALUE COUNT(*),
          'enabled_jobs' VALUE SUM(CASE WHEN enabled = 'TRUE' THEN 1 ELSE 0 END),
          'disabled_jobs' VALUE SUM(CASE WHEN enabled = 'FALSE' THEN 1 ELSE 0 END),
          'running_jobs' VALUE SUM(CASE WHEN state = 'RUNNING' THEN 1 ELSE 0 END),
          'failed_state_jobs' VALUE SUM(CASE WHEN state = 'FAILED' THEN 1 ELSE 0 END),
          'broken_jobs' VALUE SUM(CASE WHEN state = 'BROKEN' THEN 1 ELSE 0 END)
        )
        FROM USER_SCHEDULER_JOBS
        WHERE (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
      ),
      'returned_rows' VALUE (
        SELECT COUNT(*)
        FROM (
          SELECT 1
          FROM USER_SCHEDULER_JOBS
          WHERE (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
          ORDER BY job_name
          FETCH FIRST l_top_n ROWS ONLY
        )
      ),
      'truncated' VALUE (
        SELECT CASE
                 WHEN COUNT(*) > l_top_n THEN 'true'
                 ELSE 'false'
               END
        FROM USER_SCHEDULER_JOBS
        WHERE (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
      ),
      'rows' VALUE COALESCE((
        SELECT JSON_ARRAYAGG(
                 JSON_OBJECT(
                   'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
                   'job_name' VALUE job_name,
                   'enabled' VALUE enabled,
                   'state' VALUE state,
                   'job_style' VALUE job_style,
                   'job_type' VALUE job_type,
                   'job_action' VALUE job_action,
                   'program_name' VALUE program_name,
                   'schedule_name' VALUE schedule_name,
                   'job_class' VALUE job_class,
                   'repeat_interval' VALUE repeat_interval,
                   'last_start_date' VALUE TO_CHAR(last_start_date, 'YYYY-MM-DD\"T\"HH24:MI:SS TZH:TZM'),
                   'next_run_date' VALUE TO_CHAR(next_run_date, 'YYYY-MM-DD\"T\"HH24:MI:SS TZH:TZM'),
                   'run_count' VALUE run_count,
                   'failure_count' VALUE failure_count,
                   'retry_count' VALUE retry_count,
                   'max_failures' VALUE max_failures,
                   'max_runs' VALUE max_runs,
                   'auto_drop' VALUE auto_drop,
                   'restartable' VALUE restartable,
                   'logging_level' VALUE logging_level,
                   'stop_on_window_close' VALUE stop_on_window_close
                 )
               RETURNING CLOB)
        FROM (
          SELECT *
          FROM USER_SCHEDULER_JOBS
          WHERE (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
          ORDER BY job_name
          FETCH FIRST l_top_n ROWS ONLY
        )
      ), TO_CLOB('[]'))
    RETURNING CLOB) INTO l_json
    FROM dual;

    write_log('list_all_jobs', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('list_all_jobs', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END list_all_jobs;

  PROCEDURE write_log(
    p_operation_name IN VARCHAR2,
    p_start_ts       IN TIMESTAMP,
    p_status         IN VARCHAR2,
    p_error_message  IN CLOB DEFAULT NULL
  ) IS
    l_duration_ms NUMBER;
    l_delta       INTERVAL DAY TO SECOND;
  BEGIN
    l_delta := SYSTIMESTAMP - CAST(p_start_ts AS TIMESTAMP WITH TIME ZONE);
    l_duration_ms :=
        (EXTRACT(DAY FROM l_delta) * 86400000) +
        (EXTRACT(HOUR FROM l_delta) * 3600000) +
        (EXTRACT(MINUTE FROM l_delta) * 60000) +
        (EXTRACT(SECOND FROM l_delta) * 1000);
    BEGIN
      INSERT INTO SCHED_MONITOR_LOG$(operation_name, duration_ms, status, error_message)
      VALUES (p_operation_name, l_duration_ms, p_status, p_error_message);
    EXCEPTION
      WHEN OTHERS THEN
        NULL;
    END;
  END write_log;

  FUNCTION list_running_jobs(
    p_owner            IN VARCHAR2 DEFAULT NULL,
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_top_n            IN NUMBER DEFAULT 200
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_top_n NUMBER := clamp_top_n(p_top_n, 200, 5000);
  BEGIN
    SELECT JSON_OBJECT(
      'summary' VALUE JSON_OBJECT(
        'running_jobs' VALUE COUNT(*)
      ),
      'rows' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
            'job_name' VALUE job_name,
            'session_id' VALUE session_id,
            'running_instance' VALUE running_instance,
            'elapsed_time' VALUE scheduler_monitoring.format_duration(elapsed_time),
            'cpu_used_seconds' VALUE cpu_used,
            'slave_os_process_id' VALUE slave_os_process_id
          )
        ),
        JSON_ARRAY()
      )
    )
    INTO l_json
    FROM (
      SELECT job_name,
             session_id,
             running_instance,
             EXTRACT(DAY FROM elapsed_time) * 86400 + EXTRACT(HOUR FROM elapsed_time) * 3600 +
             EXTRACT(MINUTE FROM elapsed_time) * 60 + EXTRACT(SECOND FROM elapsed_time) AS elapsed_time,
             EXTRACT(DAY FROM cpu_used) * 86400 + EXTRACT(HOUR FROM cpu_used) * 3600 +
             EXTRACT(MINUTE FROM cpu_used) * 60 + EXTRACT(SECOND FROM cpu_used) AS cpu_used,
             slave_os_process_id
      FROM USER_SCHEDULER_RUNNING_JOBS
      WHERE (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
      ORDER BY elapsed_time DESC
      FETCH FIRST l_top_n ROWS ONLY
    );

    write_log('list_running_jobs', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('list_running_jobs', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END list_running_jobs;

  FUNCTION list_job_history(
    p_start_time       IN VARCHAR2,
    p_end_time         IN VARCHAR2,
    p_owner            IN VARCHAR2 DEFAULT NULL,
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_status           IN VARCHAR2 DEFAULT NULL,
    p_top_n            IN NUMBER DEFAULT 1000
  ) RETURN CLOB IS
    l_start_ts_log TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_status VARCHAR2(40) := sanitize_status(p_status);
    l_top_n NUMBER := clamp_top_n(p_top_n, 1000, 10000);
    l_from_ts TIMESTAMP WITH TIME ZONE := parse_ts(p_start_time);
    l_to_ts   TIMESTAMP WITH TIME ZONE := parse_ts(p_end_time);
  BEGIN
    IF l_from_ts IS NULL OR l_to_ts IS NULL THEN
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE 'start_time and end_time are required.');
    END IF;

    SELECT JSON_OBJECT(
      'interval' VALUE JSON_OBJECT(
        'start_time' VALUE TO_CHAR(l_from_ts, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
        'end_time' VALUE TO_CHAR(l_to_ts, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM')
      ),
      'summary' VALUE JSON_OBJECT(
        'total_runs' VALUE COUNT(*),
        'succeeded_runs' VALUE SUM(CASE WHEN status = 'SUCCEEDED' THEN 1 ELSE 0 END),
        'failed_runs' VALUE SUM(CASE WHEN status = 'FAILED' THEN 1 ELSE 0 END),
        'avg_elapsed_time' VALUE scheduler_monitoring.format_duration(ROUND(AVG(elapsed_seconds), 3)),
        'min_elapsed_time' VALUE scheduler_monitoring.format_duration(MIN(elapsed_seconds)),
        'max_elapsed_time' VALUE scheduler_monitoring.format_duration(MAX(elapsed_seconds)),
        'p95_elapsed_time' VALUE scheduler_monitoring.format_duration(ROUND(PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY elapsed_seconds), 3)),
        'avg_cpu_seconds' VALUE ROUND(AVG(cpu_seconds), 3),
        'min_cpu_seconds' VALUE MIN(cpu_seconds),
        'max_cpu_seconds' VALUE MAX(cpu_seconds),
        'p95_cpu_seconds' VALUE ROUND(PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY cpu_seconds), 3)
      ),
      'rows' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'log_id' VALUE log_id,
            'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
            'job_name' VALUE job_name,
            'status' VALUE status,
            'actual_start_time' VALUE TO_CHAR(actual_start_date, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
            'run_duration' VALUE run_duration,
            'elapsed_time' VALUE scheduler_monitoring.format_duration(elapsed_seconds),
            'cpu_seconds' VALUE cpu_seconds,
            'error_code' VALUE error#,
            'additional_info' VALUE additional_info,
            'instance_id' VALUE instance_id
          )
        ), JSON_ARRAY()
      )
    )
    INTO l_json
    FROM (
      SELECT rd.log_id,
             rd.job_name,
             rd.status,
             rd.actual_start_date,
             rd.run_duration,
             EXTRACT(DAY FROM rd.run_duration) * 86400 + EXTRACT(HOUR FROM rd.run_duration) * 3600 +
             EXTRACT(MINUTE FROM rd.run_duration) * 60 + EXTRACT(SECOND FROM rd.run_duration) AS elapsed_seconds,
             EXTRACT(DAY FROM rd.cpu_used) * 86400 + EXTRACT(HOUR FROM rd.cpu_used) * 3600 +
             EXTRACT(MINUTE FROM rd.cpu_used) * 60 + EXTRACT(SECOND FROM rd.cpu_used) AS cpu_seconds,
             rd.error#,
             rd.additional_info,
             rd.instance_id
      FROM USER_SCHEDULER_JOB_RUN_DETAILS rd
      WHERE rd.actual_start_date BETWEEN l_from_ts AND l_to_ts
        AND (p_job_name_pattern IS NULL OR rd.job_name LIKE UPPER(p_job_name_pattern))
        AND (l_status IS NULL OR rd.status = l_status)
      ORDER BY rd.actual_start_date DESC
      FETCH FIRST l_top_n ROWS ONLY
    );

    write_log('list_job_history', l_start_ts_log, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('list_job_history', l_start_ts_log, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END list_job_history;

  FUNCTION get_job_details(
    p_owner    IN VARCHAR2,
    p_job_name IN VARCHAR2
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_owner VARCHAR2(128) := UPPER(TRIM(NVL(p_owner, SYS_CONTEXT('USERENV','CURRENT_SCHEMA'))));
    l_job_name VARCHAR2(128) := UPPER(TRIM(p_job_name));
  BEGIN
    SELECT JSON_OBJECT(
      'job_metadata' VALUE (
        SELECT JSON_OBJECT(
          'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
          'job_name' VALUE j.job_name,
          'enabled' VALUE j.enabled,
          'state' VALUE j.state,
          'job_style' VALUE j.job_style,
          'job_type' VALUE j.job_type,
          'job_action' VALUE j.job_action,
          'program_name' VALUE j.program_name,
          'schedule_name' VALUE j.schedule_name,
          'job_class' VALUE j.job_class,
          'logging_level' VALUE j.logging_level,
          'restartable' VALUE j.restartable,
          'auto_drop' VALUE j.auto_drop,
          'max_failures' VALUE j.max_failures,
          'max_runs' VALUE j.max_runs,
          'repeat_interval' VALUE j.repeat_interval,
          'last_start_date' VALUE TO_CHAR(j.last_start_date, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
          'next_run_date' VALUE TO_CHAR(j.next_run_date, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
          'failure_count' VALUE j.failure_count,
          'credential_name' VALUE j.credential_name
        )
        FROM USER_SCHEDULER_JOBS j
        WHERE j.job_name = l_job_name
      ),
      'job_arguments' VALUE COALESCE((
        SELECT JSON_ARRAYAGG(
          JSON_OBJECT(
            'argument_position' VALUE a.argument_position,
            'argument_name' VALUE a.argument_name,
            'argument_type' VALUE a.argument_type,
            'value' VALUE a.value,
            'out_argument' VALUE a.out_argument
          )
        )
        FROM USER_SCHEDULER_JOB_ARGS a
        WHERE a.job_name = l_job_name
      ), JSON_ARRAY()),
      'recent_runs' VALUE COALESCE((
        SELECT JSON_ARRAYAGG(
          JSON_OBJECT(
            'log_id' VALUE r.log_id,
            'status' VALUE r.status,
            'actual_start_time' VALUE TO_CHAR(r.actual_start_date, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
            'run_duration' VALUE r.run_duration,
            'cpu_used' VALUE r.cpu_used,
            'error_code' VALUE r.error#,
            'additional_info' VALUE r.additional_info
          )
        )
        FROM (
          SELECT * FROM USER_SCHEDULER_JOB_RUN_DETAILS
          WHERE job_name = l_job_name
          ORDER BY actual_start_date DESC
          FETCH FIRST 200 ROWS ONLY
        ) r
      ), JSON_ARRAY())
    ) INTO l_json
    FROM dual;

    write_log('get_job_details', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('get_job_details', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END get_job_details;

  FUNCTION analyze_cpu_by_job(
    p_start_time       IN VARCHAR2,
    p_end_time         IN VARCHAR2,
    p_owner            IN VARCHAR2 DEFAULT NULL,
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_top_n            IN NUMBER DEFAULT 20
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_top_n NUMBER := clamp_top_n(p_top_n, 20, 500);
    l_from_ts TIMESTAMP WITH TIME ZONE := parse_ts(p_start_time);
    l_to_ts   TIMESTAMP WITH TIME ZONE := parse_ts(p_end_time);
  BEGIN
    SELECT JSON_OBJECT(
      'summary' VALUE JSON_OBJECT(
        'total_cpu_seconds' VALUE ROUND(SUM(cpu_seconds), 3),
        'total_elapsed_seconds' VALUE ROUND(SUM(elapsed_seconds), 3)
      ),
      'top_cpu_jobs' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
            'job_name' VALUE job_name,
            'runs' VALUE run_count,
            'total_cpu_seconds' VALUE total_cpu_seconds,
            'total_elapsed_time' VALUE scheduler_monitoring.format_duration(total_elapsed_seconds),
            'cpu_per_run' VALUE cpu_per_run,
            'cpu_per_minute' VALUE cpu_per_minute,
            'cpu_to_elapsed_ratio' VALUE cpu_elapsed_ratio
          )
        ), JSON_ARRAY()
      )
    )
    INTO l_json
    FROM (
      SELECT job_name,
             COUNT(*) AS run_count,
             ROUND(SUM(cpu_seconds), 3) AS total_cpu_seconds,
             ROUND(SUM(elapsed_seconds), 3) AS total_elapsed_seconds,
             ROUND(SUM(cpu_seconds)/NULLIF(COUNT(*),0), 3) AS cpu_per_run,
             ROUND((SUM(cpu_seconds)/NULLIF(SUM(elapsed_seconds),0))*60, 3) AS cpu_per_minute,
             ROUND(SUM(cpu_seconds)/NULLIF(SUM(elapsed_seconds),0), 4) AS cpu_elapsed_ratio,
             SUM(cpu_seconds) AS cpu_seconds,
             SUM(elapsed_seconds) AS elapsed_seconds
      FROM (
        SELECT rd.job_name,
               EXTRACT(DAY FROM rd.cpu_used) * 86400 + EXTRACT(HOUR FROM rd.cpu_used) * 3600 +
               EXTRACT(MINUTE FROM rd.cpu_used) * 60 + EXTRACT(SECOND FROM rd.cpu_used) AS cpu_seconds,
               EXTRACT(DAY FROM rd.run_duration) * 86400 + EXTRACT(HOUR FROM rd.run_duration) * 3600 +
               EXTRACT(MINUTE FROM rd.run_duration) * 60 + EXTRACT(SECOND FROM rd.run_duration) AS elapsed_seconds
        FROM USER_SCHEDULER_JOB_RUN_DETAILS rd
        WHERE rd.actual_start_date BETWEEN l_from_ts AND l_to_ts
          AND (p_job_name_pattern IS NULL OR rd.job_name LIKE UPPER(p_job_name_pattern))
      )
      GROUP BY job_name
      ORDER BY total_cpu_seconds DESC
      FETCH FIRST l_top_n ROWS ONLY
    );

    write_log('analyze_cpu_by_job', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('analyze_cpu_by_job', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END analyze_cpu_by_job;

  FUNCTION analyze_load_by_job(
    p_start_time       IN VARCHAR2,
    p_end_time         IN VARCHAR2,
    p_owner            IN VARCHAR2 DEFAULT NULL,
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_granularity      IN VARCHAR2 DEFAULT 'HOUR'
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_from_ts TIMESTAMP WITH TIME ZONE := parse_ts(p_start_time);
    l_to_ts   TIMESTAMP WITH TIME ZONE := parse_ts(p_end_time);
    l_gran VARCHAR2(20) := sanitize_granularity(p_granularity);
  BEGIN
    SELECT JSON_OBJECT(
      'correlation_mode' VALUE 'inferred_time_window',
      'confidence' VALUE 'medium',
      'rows' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'bucket_time' VALUE bucket_time,
            'runs' VALUE runs,
            'failed_runs' VALUE failed_runs,
            'avg_cpu_seconds' VALUE avg_cpu_seconds,
            'avg_elapsed_time' VALUE scheduler_monitoring.format_duration(avg_elapsed_seconds),
            'cpu_load_proxy' VALUE cpu_load_proxy
          )
        ), JSON_ARRAY()
      )
    )
    INTO l_json
    FROM (
      SELECT TO_CHAR(
               CASE l_gran
                 WHEN 'DAY' THEN CAST(TRUNC(CAST(rd.actual_start_date AS DATE)) AS TIMESTAMP)
                 WHEN 'HOUR' THEN CAST(TRUNC(CAST(rd.actual_start_date AS DATE), 'HH24') AS TIMESTAMP)
                 ELSE CAST(TRUNC(CAST(rd.actual_start_date AS DATE), 'MI') AS TIMESTAMP)
               END,
               'YYYY-MM-DD HH24:MI:SS'
             ) AS bucket_time,
             COUNT(*) AS runs,
             SUM(CASE WHEN rd.status = 'FAILED' THEN 1 ELSE 0 END) AS failed_runs,
             ROUND(AVG(EXTRACT(DAY FROM rd.cpu_used) * 86400 + EXTRACT(HOUR FROM rd.cpu_used) * 3600 +
                   EXTRACT(MINUTE FROM rd.cpu_used) * 60 + EXTRACT(SECOND FROM rd.cpu_used)), 3) AS avg_cpu_seconds,
             ROUND(AVG(EXTRACT(DAY FROM rd.run_duration) * 86400 + EXTRACT(HOUR FROM rd.run_duration) * 3600 +
                   EXTRACT(MINUTE FROM rd.run_duration) * 60 + EXTRACT(SECOND FROM rd.run_duration)), 3) AS avg_elapsed_seconds,
             ROUND(AVG((EXTRACT(DAY FROM rd.cpu_used) * 86400 + EXTRACT(HOUR FROM rd.cpu_used) * 3600 +
                   EXTRACT(MINUTE FROM rd.cpu_used) * 60 + EXTRACT(SECOND FROM rd.cpu_used)) /
                  NULLIF((EXTRACT(DAY FROM rd.run_duration) * 86400 + EXTRACT(HOUR FROM rd.run_duration) * 3600 +
                   EXTRACT(MINUTE FROM rd.run_duration) * 60 + EXTRACT(SECOND FROM rd.run_duration)), 0)), 4) AS cpu_load_proxy
      FROM USER_SCHEDULER_JOB_RUN_DETAILS rd
      WHERE rd.actual_start_date BETWEEN l_from_ts AND l_to_ts
        AND (p_job_name_pattern IS NULL OR rd.job_name LIKE UPPER(p_job_name_pattern))
      GROUP BY CASE l_gran
                 WHEN 'DAY' THEN TRUNC(CAST(rd.actual_start_date AS DATE))
                 WHEN 'HOUR' THEN TRUNC(CAST(rd.actual_start_date AS DATE), 'HH24')
                 ELSE TRUNC(CAST(rd.actual_start_date AS DATE), 'MI')
               END
      ORDER BY bucket_time
    );

    write_log('analyze_load_by_job', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('analyze_load_by_job', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END analyze_load_by_job;

