    l_tool_count NUMBER;
  BEGIN
    SELECT COUNT(*) INTO l_tool_count
    FROM USER_AI_AGENT_TOOLS
    WHERE TOOL_NAME = p_tool_name;

    IF l_tool_count > 0 THEN
      DBMS_CLOUD_AI_AGENT.DROP_TOOL(p_tool_name);
