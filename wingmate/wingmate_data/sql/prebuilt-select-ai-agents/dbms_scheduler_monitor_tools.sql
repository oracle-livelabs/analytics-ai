SET VERIFY OFF

VAR v_schema VARCHAR2(128)
EXEC :v_schema := '&SCHEMA_NAME';

CREATE OR REPLACE PROCEDURE initialize_scheduler_monitoring(
  p_install_schema_name IN VARCHAR2
)
IS
  l_schema_name VARCHAR2(128);

  TYPE priv_list_t IS VARRAY(20) OF VARCHAR2(4000);
  l_priv_list CONSTANT priv_list_t := priv_list_t(
    'DBMS_CLOUD_AI_AGENT',
    'DBMS_CLOUD_AI',
    'DBMS_CLOUD'
  );

  PROCEDURE execute_grants(p_schema IN VARCHAR2, p_objects IN priv_list_t) IS
    l_session_user VARCHAR2(128);
  BEGIN
    l_session_user := SYS_CONTEXT('USERENV', 'SESSION_USER');

    IF UPPER(p_schema) = UPPER(l_session_user) THEN
      DBMS_OUTPUT.PUT_LINE('Skipping grants for schema ' || p_schema ||
                           ' (same as session user).');
      RETURN;
    END IF;

    FOR i IN 1 .. p_objects.COUNT LOOP
      BEGIN
        EXECUTE IMMEDIATE 'GRANT EXECUTE ON ' || p_objects(i) || ' TO ' || p_schema;
      EXCEPTION
        WHEN OTHERS THEN
          DBMS_OUTPUT.PUT_LINE('Warning: failed to grant ' || p_objects(i) ||
                               ' to ' || p_schema || ' - ' || SQLERRM);
      END;
    END LOOP;
  END execute_grants;
BEGIN
  l_schema_name := DBMS_ASSERT.SIMPLE_SQL_NAME(p_install_schema_name);
  execute_grants(l_schema_name, l_priv_list);

  BEGIN
    EXECUTE IMMEDIATE
      'CREATE TABLE ' || l_schema_name || '.SCHED_MONITOR_LOG$(
         id               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
         logged_at        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
         operation_name   VARCHAR2(200),
         duration_ms      NUMBER,
         status           VARCHAR2(30),
         error_message    CLOB
       )';
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE != -955 THEN
        RAISE;
      END IF;
  END;

  DBMS_OUTPUT.PUT_LINE('initialize_scheduler_monitoring completed for schema ' || l_schema_name);
END initialize_scheduler_monitoring;
/

BEGIN
  initialize_scheduler_monitoring(:v_schema);
END;
/

BEGIN
  EXECUTE IMMEDIATE 'ALTER SESSION SET CURRENT_SCHEMA = ' || :v_schema;
END;
/

