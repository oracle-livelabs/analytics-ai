  FUNCTION identify_hotspots(
    p_start_time  IN VARCHAR2,
    p_end_time    IN VARCHAR2,
    p_top_n       IN NUMBER DEFAULT 20,
    p_min_elapsed IN NUMBER DEFAULT 0,
    p_min_cpu     IN NUMBER DEFAULT 0
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_top_n NUMBER := clamp_top_n(p_top_n, 20, 500);
    l_from_ts TIMESTAMP WITH TIME ZONE := parse_ts(p_start_time);
    l_to_ts   TIMESTAMP WITH TIME ZONE := parse_ts(p_end_time);
  BEGIN
    SELECT JSON_OBJECT(
      'rows' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
            'job_name' VALUE job_name,
            'runs' VALUE runs,
            'total_cpu_seconds' VALUE total_cpu_seconds,
            'total_elapsed_time' VALUE scheduler_monitoring.format_duration(total_elapsed_seconds),
            'avg_cpu_to_elapsed_ratio' VALUE cpu_elapsed_ratio,
            'hotspot_reason' VALUE hotspot_reason
          )
        ), JSON_ARRAY()
      )
    ) INTO l_json
    FROM (
      SELECT job_name,
             COUNT(*) runs,
             ROUND(SUM(cpu_seconds), 3) total_cpu_seconds,
             ROUND(SUM(elapsed_seconds), 3) total_elapsed_seconds,
             ROUND(AVG(cpu_seconds/NULLIF(elapsed_seconds,0)), 4) cpu_elapsed_ratio,
             CASE
               WHEN SUM(cpu_seconds) >= p_min_cpu AND SUM(elapsed_seconds) >= p_min_elapsed THEN 'high_cpu_and_long_runtime'
               WHEN SUM(cpu_seconds) >= p_min_cpu THEN 'high_cpu'
               ELSE 'long_runtime'
             END hotspot_reason
      FROM (
        SELECT job_name,
               EXTRACT(DAY FROM cpu_used) * 86400 + EXTRACT(HOUR FROM cpu_used) * 3600 +
               EXTRACT(MINUTE FROM cpu_used) * 60 + EXTRACT(SECOND FROM cpu_used) AS cpu_seconds,
               EXTRACT(DAY FROM run_duration) * 86400 + EXTRACT(HOUR FROM run_duration) * 3600 +
               EXTRACT(MINUTE FROM run_duration) * 60 + EXTRACT(SECOND FROM run_duration) AS elapsed_seconds
        FROM USER_SCHEDULER_JOB_RUN_DETAILS
        WHERE actual_start_date BETWEEN l_from_ts AND l_to_ts
      )
      GROUP BY job_name
      HAVING SUM(elapsed_seconds) >= NVL(p_min_elapsed, 0)
         OR SUM(cpu_seconds) >= NVL(p_min_cpu, 0)
      ORDER BY total_cpu_seconds DESC, total_elapsed_seconds DESC
      FETCH FIRST l_top_n ROWS ONLY
    );

    write_log('identify_hotspots', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('identify_hotspots', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END identify_hotspots;

  FUNCTION detect_failures(
    p_start_time       IN VARCHAR2,
    p_end_time         IN VARCHAR2,
    p_owner            IN VARCHAR2 DEFAULT NULL,
    p_job_name_pattern IN VARCHAR2 DEFAULT NULL,
    p_top_n            IN NUMBER DEFAULT 100
  ) RETURN CLOB IS
    l_start_ts TIMESTAMP := SYSTIMESTAMP;
    l_json CLOB;
    l_top_n NUMBER := clamp_top_n(p_top_n, 100, 2000);
    l_from_ts TIMESTAMP WITH TIME ZONE := parse_ts(p_start_time);
    l_to_ts   TIMESTAMP WITH TIME ZONE := parse_ts(p_end_time);
  BEGIN
    SELECT JSON_OBJECT(
      'summary' VALUE JSON_OBJECT(
        'failed_runs' VALUE COUNT(*),
        'distinct_jobs_failed' VALUE COUNT(DISTINCT job_name)
      ),
      'top_failures' VALUE COALESCE(
        JSON_ARRAYAGG(
          JSON_OBJECT(
            'owner' VALUE SYS_CONTEXT('USERENV','CURRENT_SCHEMA'),
            'job_name' VALUE job_name,
            'failures' VALUE failures,
            'last_failure_time' VALUE TO_CHAR(last_failure_time, 'YYYY-MM-DD"T"HH24:MI:SS TZH:TZM'),
            'common_error_code' VALUE common_error_code,
            'common_error_message' VALUE common_error_message
          )
        ), JSON_ARRAY()
      )
    )
    INTO l_json
    FROM (
      SELECT job_name,
             COUNT(*) failures,
             MAX(actual_start_date) last_failure_time,
             MIN(error#) KEEP (DENSE_RANK FIRST ORDER BY COUNT(*) DESC) common_error_code,
             MIN(additional_info) KEEP (DENSE_RANK FIRST ORDER BY COUNT(*) DESC) common_error_message
      FROM USER_SCHEDULER_JOB_RUN_DETAILS
      WHERE actual_start_date BETWEEN l_from_ts AND l_to_ts
        AND status = 'FAILED'
        AND (p_job_name_pattern IS NULL OR job_name LIKE UPPER(p_job_name_pattern))
      GROUP BY job_name
      ORDER BY failures DESC, last_failure_time DESC
      FETCH FIRST l_top_n ROWS ONLY
    );

    write_log('detect_failures', l_start_ts, 'SUCCESS');
    RETURN l_json;
  EXCEPTION
    WHEN OTHERS THEN
      write_log('detect_failures', l_start_ts, 'ERROR', SQLERRM);
      RETURN JSON_OBJECT('status' VALUE 'error', 'message' VALUE SQLERRM);
  END detect_failures;

