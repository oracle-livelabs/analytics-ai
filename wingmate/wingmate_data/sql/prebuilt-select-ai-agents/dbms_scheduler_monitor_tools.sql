                 EXTRACT(DAY FROM cpu_used) * 86400 + EXTRACT(HOUR FROM cpu_used) * 3600 +
                 EXTRACT(MINUTE FROM cpu_used) * 60 + EXTRACT(SECOND FROM cpu_used) AS cpu_seconds
